/** buildSystemPrompt のオプション（将来の差し込み用。今は呼び名のみ任意で対応）。 */
export interface PersonaOptions {
  /** 利用者の呼び名（例:「はなさん」）。指定があれば呼びかけに使うよう促す。 */
  nickname?: string;
}

/**
 * システムプロンプトを組み立てる。
 * 今は固定文がベース。将来、呼び名・声・話し方などを差し込めるよう関数形にしてある。
 */
export function buildSystemPrompt(opts: PersonaOptions = {}): string {
  let prompt = SYSTEM_PROMPT;
  if (opts.nickname && opts.nickname.trim().length > 0) {
    prompt += `\n\n【この方の呼び名】\n- この方の呼び名は「${opts.nickname.trim()}」です。折にふれてやさしく呼びかけてください。`;
  }
  return prompt;
}
